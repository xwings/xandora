<h1>About xandora.net</h1> 
<p>
xandora.net is a tool for analyzing the behavior of Windows PE-executables with special focus on the analysis of malware. Execution of xandora.net results in the generation of a report file that contains enough information to give a human user a very good impression about the purpose and the actions of the analyzed binary.</p>

<p>
The generated report includes detailed data about modifications made to the Windows registry or the file system or other processes and of course it logs all generated network traffic. The analysis is based on running the binary in an emulated environment and watching.</p>

<p>	
It is the ideal tool for a person to get a quick understanding of the purpose of an unknown binary.</p>

